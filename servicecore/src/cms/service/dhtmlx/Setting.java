package cms.service.dhtmlx;


public class Setting {
	
	private String colwidth;
    
	public Setting(String colwidth) {
	
		this.colwidth = colwidth;
	}

	
	public Setting() {
		
	}


	public String getColwidth() {
		return colwidth;
	}

	public void setColwidth(String colwidth) {
		this.colwidth = colwidth;
	}

}
